document.getElementById("calb").onclick = function(){
    let sr = document.getElementById("a0").value;
    sr = Number(sr);
    document.getElementById("a1").innerHTML = sr;
    document.getElementById("a2").innerHTML = sr;
    let dm = document.getElementById("b0").value;
    dm = Number(dm);
    document.getElementById("b1").innerHTML = dm;
    let dl = document.getElementById("c0").value;
    dl = Number(dl);
    document.getElementById("c1").innerHTML = dl;
    let de = document.getElementById("d0").value;
    de = Number(de);
    document.getElementById("d1").innerHTML = de;
    let vmo = document.getElementById("e0").value;
    vmo = Number(vmo);
    document.getElementById("e1").innerHTML = vmo;
    let s1 = dm + dl + de + vmo ;
    s1 = Number(s1);
    let os = document.getElementById("f0").value;
    document.getElementById("f1").innerHTML = os;
    os = Number(os);
    let cs = document.getElementById("g0").value;
    cs = Number(cs);
    document.getElementById("g1").innerHTML = cs;
    let s11 = s1 + os;
    s11 = Number(s11);
    let s12 = s11 - cs;
    s12 = Number(s12);
    let gcm = sr - s12;
    gcm = Number(gcm);
    document.getElementById("gcm").innerHTML = gcm;
    let vsdo = document.getElementById("h0").value;
    vsdo = Number(vsdo);
    document.getElementById("h1").innerHTML = vsdo;
    let vaoe = document.getElementById("i0").value;
    vaoe = Number(vaoe);
    document.getElementById("i1").innerHTML = vaoe;
    let pre = vsdo + vaoe;
    pre = Number(pre);
    let s2 = gcm - pre;
    s2 = Number(s2);
    document.getElementById("ncm").innerHTML = s2;
    let fmo = document.getElementById("j0").value;
    fmo = Number(fmo);
    document.getElementById("j1").innerHTML = fmo;
    let foa = document.getElementById("k0").value;
    foa = Number(foa);
    document.getElementById("k1").innerHTML = foa;
    let fsd = document.getElementById("l0").value;
    fsd = Number(fsd);
    document.getElementById("l1").innerHTML = fsd;
    let s3 = fmo + foa + fsd;
    s3 = Number(s3);
    document.getElementById("l2").innerHTML = s3;
    document.getElementById("ans").innerHTML = s2 - s3;


}
